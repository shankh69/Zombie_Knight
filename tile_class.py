class Tile(pygame.sprite.Sprite):

    def __init__(self, x, y, tilemap):
        super().__init__()

        self.image = pygame.transform.scale(pygame.image.load(f"zombie_knight_assets/images/tiles/Tile ({tilemap}).png")
                                            , (32, 32))
        self.rect = self.image.get_rect()
        self.rect.topleft = (x, y)

    def create_mask(self):
        self.mask = pygame.mask.from_surface(self.image)



main_tile_group = pygame.sprite.Group()

platform_group = pygame.sprite.Group()

# create tile map
# 0 > no tile, 1 > dirt, 2-5 > platforms, 6 > ruby, 7-8 > platform, 9 > player
# 23 rows, 43 columns

for i in range(len(tilemap)):
    for j in range(len(tilemap[0])):
        if tilemap[i][j] != 0:
            tile = Tile(j*32, i*32, tilemap[i][j])
            main_tile_group.add(tile)
            if tilemap[i][j] == 5 or tilemap[i][j] == 2 or tilemap[i][j] == 3 or tilemap[i][j] == 4:
                platform_group.add(tile)
                tile.create_mask()